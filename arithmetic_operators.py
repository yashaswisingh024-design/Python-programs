# Arithmetic Operators in Python

# Define two numbers
a = 15
b = 4

# 1. Addition (+)
addition = a + b
print(f"Addition: {a} + {b} = {addition}")

# 2. Subtraction (-)
subtraction = a - b
print(f"Subtraction: {a} - {b} = {subtraction}")

# 3. Multiplication (*)
multiplication = a * b
print(f"Multiplication: {a} * {b} = {multiplication}")

# 4. Division (/)
division = a / b
print(f"Division: {a} / {b} = {division}")

# 5. Floor Division (//)
floor_division = a // b
print(f"Floor Division: {a} // {b} = {floor_division}")

# 6. Modulus (%)
modulus = a % b
print(f"Modulus: {a} % {b} = {modulus}")

# 7. Exponentiation (**)
exponentiation = a ** b
print(f"Exponentiation: {a} ** {b} = {exponentiation}")

# Combined operations with operator precedence
result = (a + b) * 2 - a / b
print(f"\nCombined: ({a} + {b}) * 2 - {a} / {b} = {result}")
